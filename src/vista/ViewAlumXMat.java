    private void jComboMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboMateriaActionPerformed
        borrarTabla();
        String selectedItem = jComboMateria.getSelectedItem().toString();
        String[] parts = selectedItem.split(" ");
        int idM = Integer.parseInt(parts[0]);
        List<Alumno> lisa = inscripData.obtenerAlumnosXMateria(idM);
        String fila[] = new String[3];
        Double nota=0.0;
        List<Inscripcion> lis;
        for (int i = 0; i < lisa.size(); i++) {
            fila[0] = lisa.get(i).getIdAlumno() + "";
            fila[1] = lisa.get(i).getNombre();
            lis = inscripData.obtenerInscripcionesPorAlumno(lisa.get(i).getIdAlumno());
            for (Inscripcion li : lis) {
                if (li.getMateriaId().getIdMateria()==idM && li.getAlumnoId().getIdAlumno()==lisa.get(i).getIdAlumno()) {
                    nota = li.getNota();
                }
            }
            fila[2] = nota+"";
            tabla.addRow(fila);
        }
        jLista.setModel(tabla);
    }//GEN-LAST:event_jComboMateriaActionPerformed



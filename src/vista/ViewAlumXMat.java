    public void crearTabla() {
        ArrayList<Object> columnas = new ArrayList<Object>();
        columnas.add("ID");
        columnas.add("nombre");
        columnas.add("nota");
        for (Object iter : columnas) {
            tabla.addColumn(iter);
        }
        jLista.setModel(tabla);
    }

    public void borrarTabla() {
        int a = tabla.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tabla.removeRow(i);
        }
    }

    public void agregarMateria() {
        Materia lista = new Materia();
        for (Iterator<Materia> it = listaM.iterator(); it.hasNext();) {
            lista = it.next();
            jComboMateria.addItem(lista.getIdMateria() + " " + lista.getNombre() + " " + lista.getAnio() + " " + lista.isEstado());
        }
    }
